const locales = ['en', 'es', 'zh', 'hi', 'ar', 'fr', 'pt', 'ru', 'ja', 'de'];

let allOk = true;
for (const locale of locales) {
  const filePath = path.join(i18nDir, locale + '.json');
  const raw = fs.readFileSync(filePath, 'utf8');
  const data = JSON.parse(raw);

  // Merge new keys into existing PROFILE object (preserve any existing keys)
  data.PROFILE = Object.assign({}, data.PROFILE || {}, profileKeys[locale]);

  const newJson = JSON.stringify(data, null, 2);
  // Validate before writing
  try {
    JSON.parse(newJson);
  } catch (e) {
    console.error('VALIDATION FAILED for', locale, e);
    allOk = false;
    continue;
  }
  fs.writeFileSync(filePath, newJson + '\n', 'utf8');
  console.log('Updated', locale, '— PROFILE keys:', Object.keys(data.PROFILE).length);
}

if (allOk) {
  console.log('\nAll locale files updated and validated successfully.');
} else {
  console.error('\nSome files failed validation!');
  process.exit(1);
}
