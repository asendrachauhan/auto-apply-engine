const localeDir = path.join(__dirname, 'src/assets/i18n');
const langs = ['en', 'es', 'zh', 'hi', 'ar', 'fr', 'pt', 'ru', 'ja', 'de'];

langs.forEach(lang => {
  const filePath = path.join(localeDir, `${lang}.json`);
  const data = JSON.parse(fs.readFileSync(filePath, 'utf8'));
  
  // Add EU_CAREERS section
  data['EU_CAREERS'] = translations[lang];
  
  // Also add PLANS.MANAGE_BILLING_DESC if it doesn't exist
  if (!data['PLANS']['MANAGE_BILLING_DESC']) {
    data['PLANS']['MANAGE_BILLING_DESC'] = translations[lang]['MANAGE_BILLING_DESC'];
  }
  
  const json = JSON.stringify(data, null, 2);
  
  // Verify it's valid JSON before writing
  JSON.parse(json);
  
  fs.writeFileSync(filePath, json + '\n', 'utf8');
  console.log(`✅ Updated ${lang}.json`);
});

console.log('\nAll locale files updated successfully!');
